const featureIconMap: Record<IconName, React.ComponentType<IconProps>> = {
  computer: ComputerIcon,
  clock: ClockIcon,
  shield: ShieldIcon,
  'chart-up': ChartUpIcon,
  'pie-chart': PieChartIcon,
  users: UsersIcon,
  'arrow-right': ArrowRightIcon,
  play: PlayIcon,
  check: CheckIcon,
};

const socialIconMap: Record<SocialIconName, React.ComponentType<{ className?: string }>> = {
  twitter: TwitterIcon,
  github: GitHubIcon,
  discord: DiscordIcon,
};

export function Icon({ name, ...props }: IconProps & { name: IconName }) {
  const IconComponent = featureIconMap[name];
  return <IconComponent {...props} />;
}

export function SocialIcon({ name, ...props }: { name: SocialIconName; className?: string }) {
  const IconComponent = socialIconMap[name];
  return <IconComponent {...props} />;
}
